public class ForEach {
    public static void main(String[] args) {
        /**
         * ada juga perulangan namanya ForEach kebanyakan sih digunakan untuk mengambil index pada array
         */
        String[] arr = {
                "Naufal", "Abdul","Basth"
        };

        for (String result : arr){
            System.out.println(result);
        }
    }
}
