public class WhileLoop {
    public static void main(String[] args) {
        /**
         * While loop memiliki fungsi yang sama dengan for loop namun while loop di tulis
         * dengan kode yang cukup singkat
         */
        int count = 1;
        while (count <= 10){
            System.out.println("Perhitungan " + count);
            count++;
        }
        //yahh kalian hanya perlu menambahkan kodisi setelah kata kunci while maka program loop akan berjalan

    }
}
