import java.util.Date;

public class Variables {
    public static void main(String[] args) {
        /**
         * variable adalah tempat untuk menyimpan nilai yang akan di gunakan
         */
        String nama = "Naufal";
        System.out.println(nama);
        nama = "Abdul";
        System.out.println(nama);

        /**
         * Namun ada juga Variable Object gunanya bisa di gunakan untuk penggunaan
         * object pada suatu class
         */
        Date date = new Date();
    }
}
