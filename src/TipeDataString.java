public class TipeDataString {
    public static void main(String[] args) {
        /**
         * Tipe data string merupakan tipe data non primitif yang di gunakan untuk
         * menampilkan text atau mengubah text dan sebagian nya
         * mari kita praktekan langsung
         */
        String nama = "Naufal";
        String namaTengah = "Abdul";
        System.out.println(nama);
        System.out.println(namaTengah);
        /**
         *  Kalian juga bisa menyatukan dua tipe data string bersamaan
         */

        String result = nama + " " + namaTengah;
        // jangan lupa penyatuan string hanya bisa menggunakan operasi penjumlahan
        System.out.println(result);
    }
}
