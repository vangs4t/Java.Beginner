public class Perbandingan {
    public static void main(String[] args) {
        /**
         * Yah kalian tau sendiri lah ya karena sudah di ajarkan sejak
         * sekolah dasar
         */
        int A = 100;
        int B = 10;

        System.out.println(A > B);
        // A lebih besar dari B true
        System.out.println(A < B);
        // A lebih kecil dari B false
        System.out.println(A >= B);
        // A lebih besar atau sama sengan B true
        System.out.println(A <= B);
        // A lebih kecil atau sama denga B false
        System.out.println(A == B);
        // A sama dengan B false
        System.out.println(A != B);
        // A tidak sama dengan B true
    }
}
