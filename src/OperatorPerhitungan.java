public class OperatorPerhitungan {
    public static void main(String[] args) {
        System.out.println("Penjumlahan dari 2 + 3 = " + (2 + 3));
        System.out.println("Pengurangan dari 5 - 2 = " +(5 - 2));
        System.out.println("Perkalian dari 5 * 2 = " +(5 * 2));
        System.out.println("Pembagian dari 6 / 2 = " +(6 / 2));
        System.out.println("Modulus dari 7 % 2 = " + (7 % 2));
    }
}
